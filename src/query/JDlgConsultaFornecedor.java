    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:                    
                 if(!jTxtNome.getText().equals("")){
            List lista= fornecedorGabDAO.listNome(jTxtNome.getText());
            fornecedorControle_gab.setList(lista);
        } else {
            if (!jTxtNome.getText().equals("") && !jFmtData.getText().equals("")) {
                Date data = Util_gab.strDate(jFmtData.getText());
                List lista = fornecedorGabDAO.listNomeData(data, jTxtNome.getText());
                fornecedorControle_gab.setList(lista);
            } else {
                if (!jTxtNome.getText().equals("")) {
                    List lista = fornecedorGabDAO.listNome(jTxtNome.getText());
                    fornecedorControle_gab.setList(lista);
                } else {
                    if (!jFmtData.getText().equals("")) {  
                    Date data = Util_gab.strDate(jFmtData.getText());
                    List lista = fornecedorGabDAO.listData(data);
                    fornecedorControle_gab.setList(lista);
                    }
                }
            }
        }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

